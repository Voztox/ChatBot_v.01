package com.howtodoinjava.demo.chatbot;

public class Locations {
	
	//Initialize private variables
	private String city; //string
	private double temp; //in degree 
	private int precipitation; //in percentage (of rain)
	private int humidity; //in percentages (amount moisture).
	private double wind; //km/h (strength of wind).
	
	
	public Locations(String city) {
		this.city = city;
		this.temp = temp;
		this.precipitation = precipitation;
		this.humidity = humidity;
		this.wind = wind;
	}
	
}
